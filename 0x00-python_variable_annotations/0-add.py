#!/usr/bin/env python3
"""a type-annotated function add"""


def add(a: float, b: float) -> float:
    """
        A type-annotated function add that takes a float a and
        float b as arguments and returns their sum as a float.
        arguments:
                a(float): the first float value.
                b(float): the second float value
    """

    return a + b
